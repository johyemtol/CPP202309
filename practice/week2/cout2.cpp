#include <iostream>
#include <string>
using namespace std;

int main() {
	char character = 'C';
	int integer = 100;
	double precision = 3.14159;
	bool is_true = true;
	string word = "Hello World";

	//cout 통해 위에 저장해둔 값들 출력하기
	cout << "char : " << character << endl;
	cout << "int : " << integer << endl;
	cout << "double : " << precision << endl;
	cout << "bool : " << is_true << endl;
	cout << "string : " << word << endl;

	return 0;
}
